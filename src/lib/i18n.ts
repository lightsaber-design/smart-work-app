export const translations = { es };

// Loaded language translations cache
const _cache: Partial<Record<Lang, TranslationMap>> = { es };
const _listeners: Array<() => void> = [];

export async function loadLanguage(lang: Lang): Promise<void> {
  if (lang === 'es' || _cache[lang]) return;
  let mod: { default: TranslationMap };
  switch (lang) {
    case 'en': mod = await import('./i18n/en'); break;
    case 'pt': mod = await import('./i18n/pt'); break;
    case 'fr': mod = await import('./i18n/fr'); break;
    case 'it': mod = await import('./i18n/it'); break;
    case 'de': mod = await import('./i18n/de'); break;
    default: return;
  }
  _cache[lang] = mod.default;
  _listeners.forEach((cb) => cb());
}

export function subscribeLanguageLoad(cb: () => void): () => void {
  _listeners.push(cb);
  return () => { const i = _listeners.indexOf(cb); if (i >= 0) _listeners.splice(i, 1); };
}

export type TranslationKey = keyof typeof es;

export function translate(lang: Lang, key: string, vars?: Record<string, string | number>): string {
  let str = (_cache[lang]?.[key] ?? _cache.en?.[key] ?? es[key]) ?? key;
  if (vars) Object.entries(vars).forEach(([k, v]) => { str = str.replace(`{${k}}`, String(v)); });
  return str;
}
