export function detectLanguage(): Lang {
  const nav = navigator.language || '';
  const code = nav.split('-')[0].toLowerCase();
  const supported: Lang[] = ['es', 'en', 'pt', 'fr', 'it', 'de'];
  return supported.includes(code as Lang) ? (code as Lang) : 'es';
}

