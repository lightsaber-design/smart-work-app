export function translate(lang: Lang, key: string, vars?: Vars): string {
  let str = translations[lang]?.[key] ?? translations['en']?.[key] ?? translations['es']?.[key] ?? key;
  if (vars) {
    Object.entries(vars).forEach(([k, v]) => {
      str = str.replace(`{${k}}`, String(v));
    });
  }
  return str;
}
